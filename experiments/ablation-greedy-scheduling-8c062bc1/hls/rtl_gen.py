"""
Verilog RTL emission for the greedy-scheduled architecture.

Emits one synthesizable-style module per benchmark: a cycle counter (the
FSM), one dedicated result register per DAG node, `resource_limits[op]`
instances of each functional-unit type (op-node -> unit binding from
hls/binding.py), and one `active_glitch_latch` instance per
hls/latches.py LatchSpec on the flagged operand.

No physical EDA toolchain (Yosys/Verilator) is available in this sandbox
(no root, no network access to apt) -- this is documented explicitly in
code/README.md and report.md rather than silently faked. The RTL here is
therefore a faithful, auditable text rendering of the internal schedule/
binding/latch model (used for area/power/TVLA estimation via the custom
Python models in hls/area_model.py, hls/power_model.py, hls/tvla.py), not
something we claim was run through real synthesis.
"""

HEADER = """\
// Auto-generated by code/train.py (Greedy-Latch-Compiler ablation arm).
// Benchmark: {name}
// Scheduler: greedy list scheduling (ablation-greedy-scheduling)
// Latency: {latency} cycles | Functional units: {units}
// Active glitch-stopping latches inserted: {n_latches}
// NOTE: rendering of the internal schedule/binding/latch model; not
// verified by a physical synthesis toolchain (none available in this
// sandbox -- see code/README.md).
module {name} (
    input  wire              clk,
    input  wire              rst_n,
    input  wire [{wm1}:0]    s0,        // masking share A of the secret input
    input  wire [{wm1}:0]    s1,        // masking share B of the secret input
{pub_ports}
    output reg  [{wm1}:0]    result_valid_out,
    output reg               done
);

    reg [{fsm_bits}:0] cycle_cnt;
{regs}
{latches}
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            cycle_cnt <= 0;
            done <= 1'b0;
        end else if (!done) begin
            cycle_cnt <= cycle_cnt + 1;
            case (cycle_cnt)
{cases}
                default: ;
            endcase
            if (cycle_cnt == {latency} - 1) begin
                done <= 1'b1;
                result_valid_out <= {output_reg};
            end
        end
    end

endmodule
"""

LATCH_MODULE = """\
// Register-free, dynamically-controlled active glitch-stopping latch.
// Transparent while `enable` is high (from open_cycle), then holds its
// sampled value stable through close_cycle so the consumer never
// observes a glitch from an early-arriving operand.
module active_glitch_latch #(parameter W = 16) (
    input  wire         clk,
    input  wire         enable,
    input  wire [W-1:0] d,
    output reg  [W-1:0] q
);
    always @(*) begin
        if (enable) q = d;
    end
endmodule
"""


def _op_symbol(op):
    return {"ADD": "+", "SUB": "-", "MUL": "*"}[op]


def generate_verilog(bench, cycle_of, unit_of, latch_specs, width=16):
    op_nodes = [nid for nid in bench.order if bench.nodes[nid].op != "IN"]
    latched_inputs = {(ls.consumer, ls.held_input) for ls in latch_specs}

    reg_lines = []
    for nid in op_nodes:
        reg_lines.append(f"    reg [{width - 1}:0] r_{nid}; // cycle {cycle_of[nid]} "
                          f"unit {bench.nodes[nid].op.lower()}_u{unit_of[nid]}")
    regs = "\n".join(reg_lines)

    latch_lines = []
    for i, ls in enumerate(latch_specs):
        latch_lines.append(
            f"    wire [{width - 1}:0] lq_{i};\n"
            f"    active_glitch_latch #(.W({width})) latch_{i} ("
            f".clk(clk), .enable(cycle_cnt == {ls.open_cycle}), "
            f".d(r_{ls.held_input}), .q(lq_{i})); "
            f"// holds {ls.held_input} for consumer {ls.consumer} until cycle {ls.close_cycle}"
        )
    latches = "\n".join(latch_lines)

    def operand_ref(consumer, inp):
        key = (consumer, inp)
        if key in latched_inputs:
            idx = [i for i, ls in enumerate(latch_specs)
                   if ls.consumer == consumer and ls.held_input == inp][0]
            return f"lq_{idx}"
        return f"r_{inp}" if bench.nodes[inp].op != "IN" else inp

    case_lines = []
    by_cycle = {}
    for nid in op_nodes:
        by_cycle.setdefault(cycle_of[nid], []).append(nid)
    for t in sorted(by_cycle):
        case_lines.append(f"                {t}: begin")
        for nid in sorted(by_cycle[t]):
            node = bench.nodes[nid]
            a = operand_ref(nid, node.inputs[0])
            b = operand_ref(nid, node.inputs[1])
            case_lines.append(f"                    r_{nid} <= {a} {_op_symbol(node.op)} {b};"
                               f" // unit {node.op.lower()}_u{unit_of[nid]}")
        case_lines.append("                end")
    cases = "\n".join(case_lines)

    pub_ports = "\n".join(
        f"    input  wire [{width - 1}:0] {pid}," for pid in bench.primary_inputs
    )
    latency = max(cycle_of.values()) + 1
    output_ref = (f"r_{bench.output}" if bench.nodes[bench.output].op != "IN"
                  else bench.output)

    body = HEADER.format(
        name=bench.name,
        latency=latency,
        units=bench.resource_limits,
        n_latches=len(latch_specs),
        wm1=width - 1,
        pub_ports=pub_ports,
        fsm_bits=max(4, latency.bit_length()),
        regs=regs,
        latches=latches,
        cases=cases,
        output_reg=output_ref,
    )
    return LATCH_MODULE + "\n" + body
